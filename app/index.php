                        <div id="tabs-5">
                            <h4>Подбор ключевых слов при SEO продвижении</h4>

                            <p>При составлении семантического ядра сайта основополагающими должны быть следующие правила:</p>
                            <ol>
                                <li>Каждый запрос полностью соответствует тематике.</li>
                                <li>Не должно быть общих информационных запросов.</li>
