                            <h4>Низкочастотное продвижение сайтов</h4>

                            <p>
                                Как правило, почти все веб-дизайнеры стремятся продвигать свой ресурс в ТОП по высокочастотным запросам. Это кажется полностью логичным: чем больше людей попадут на сайт благодаря тому, что он будет первым в выдаче по очень популярному запросу, тем большим будет постоянный трафик. Однако, специалисты в последнее время, после того, как уже научились действительно необычайно эффективно выводить сайты в ТОП-10, указывают, что самая большая проблема заключается в том, что при высокочастотном запросе формируется не очень качественный траффик. По высокочастотным запросам люди, как правило, ищут не товар для покупки и не конкретный ресурс, на котором они готовы что-то сделать, например, вступить в комьюнити, а только информацию, которая им нужна. Таким образом, для информационных, новостных сайтов, библиотек и других подобных видов сайтов высокочастоные запросы будут действительно эффективным средством создания именно целевого трафика. А вот для полноценных Интернет-магазинов, корпоративных сайтов и т. д. он почти бесполезен и даже часто вызывает раздражение пользователя.
                            </p>

                            <p>
                                С другой стороны, низкочастотные запросы отличаются тем, что они намного более подробные и человек, когда его вводит, чаще всего ищет именно необходимую для него вещь, нужный предмет или услугу, которую он уже, по большому счёту, готов купить. Кроме того, высокое качество запроса позволяет очень точно делить аудиторию по разным страницам сайта, направляя каждого с его запросом непосредственно к тому товару, который этого пользователя интересует. Колоссальная роль, которую играет такой подход, позволяет добиваться успеха в большинстве случаев, если правильно выбраны поисковые фразы.
                            </p>

                            <p>
                                Для продвижения низкочастотными запросами желательно очень точно определить эти запросы. Они, как правило, намного более сложные и включаются в себя не одно слово или даже словосочетание, а несколько слов или даже фразу. Для того, чтобы точно таргетировать аудиторию, желательно сначала создать механизмы изучения потребностей этой аудитории, то есть, составить представление о том, каким именно образом люди ищут то, что мы готовы им продать. Для этого можно провести фокус-группы, или купить соответствующую информацию по запросам у поисковых машин, которые эту информацию всё-равно накапливают. В некоторых случаях информацию по запросам можно посмотреть даже бесплатно.
